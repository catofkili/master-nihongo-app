const sortedRules = [...rubyRules].sort((left, right) => right[0].length - left[0].length);

type RubyNode = string | { base: string; reading: string };

// 原有逻辑:按固定词典逐字扫描
const scanWithDict = (text: string): RubyNode[] => {
  const nodes: RubyNode[] = [];
  let index = 0;
  while (index < text.length) {
    const rule = sortedRules.find(([base]) => text.startsWith(base, index));
    if (!rule) {
      nodes.push(text[index]);
      index += 1;
      continue;
    }
    nodes.push({ base: rule[0], reading: rule[1] });
    index += rule[0].length;
  }
  return nodes;
};

// 后端 pykakasi 生成的注音标记:⟪原文｜读音⟫
const RUBY_MARKUP = /⟪([^｜⟫]+)｜([^⟫]+)⟫/g;

export const JapaneseRuby = ({ text }: { text: string }) => {
  const nodes: RubyNode[] = [];
  let last = 0;
  let match: RegExpExecArray | null;
  RUBY_MARKUP.lastIndex = 0;
  while ((match = RUBY_MARKUP.exec(text)) !== null) {
    if (match.index > last) nodes.push(...scanWithDict(text.slice(last, match.index)));
    nodes.push({ base: match[1], reading: match[2] });
    last = match.index + match[0].length;
  }
  if (last < text.length) nodes.push(...scanWithDict(text.slice(last)));

  return (
    <>
      {nodes.map((node, nodeIndex) =>
        typeof node === "string" ? (
          <span key={`${node}-${nodeIndex}`}>{node}</span>
        ) : (
          <ruby key={`${node.base}-${nodeIndex}`} className="jp-ruby">
            {node.base}
            <rt>{node.reading}</rt>
          </ruby>
        )
      )}
    </>
  );
};
